package projects;

public class StudentFeeDetails {

	
	String student_name,branch;
	int year;
	long tution_fee,mba_fee,bus_fee,hostel_fee;
	
	
	
	
	
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	
	
	
	
	
	
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	
	
	
	
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
	
	
	
	public long getTution_fee() {
		return tution_fee;
	}
	public void setTution_fee(long tution_fee) {
		this.tution_fee = tution_fee;
	}
	
	
	
	
	public long getMba_fee() {
		return mba_fee;
	}
	public void setMba_fee(long mba_fee) {
		this.mba_fee = mba_fee;
	}
	
	
	
	
	public long getBus_fee() {
		return bus_fee;
	}
	public void setBus_fee(long bus_fee) {
		this.bus_fee = bus_fee;
	}
	
	
	
	public long getHostel_fee() {
		return hostel_fee;
	}
	public void setHostel_fee(long hostel_fee) {
		this.hostel_fee = hostel_fee;
	}
	
	
	
	
	
}
